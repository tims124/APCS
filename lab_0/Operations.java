class Operations{
	public static void main(String[] args){
		int a=13;
		int b=5;
		double c=13;
		double d=5.0;
		System.out.println("a+b="+(a+b));
		System.out.println("c+d="+(c+d));
		System.out.println("a-b="+(a-b));
		System.out.println("a*b="+(a*b));
		System.out.println("a+c="+(a+c));
		System.out.println("a/b="+(a/b));
		System.out.println("c/d="+(c/d));
		System.out.println("a/d="+(a/d));
		System.out.println("a%b="+(a%b));
	}
}